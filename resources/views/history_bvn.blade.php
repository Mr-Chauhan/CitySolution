      <p>
        The History of Bhavnagar goes back a lot further then the city itself. It begins in the 12th-13th century, when the Gohil Rajputs of the Surya Vanshi clan, facing severe competition in Marwar, moved down to the Gujarat coast and finally settled at Gogha port, near present-day Bhavnagar.
        </p>
        <p>
        Bhavnagar was founded by Bhavsinhji Gohil, the rulers of Sihor in 1723 AD. It was the time of persistent Maratha raids of the States of Saurashtra. The last princely ruler Late Sir Krishnakumarsinhji handed over the administration of his State to the people's representative in 1948 on the advice of Gandhiji. Founded as a port in 1743, Bhavnagar is still an important trading post for the cotton goods manufactured in Gujarat. The Bhavnagar lock gate keeps ships a float in the city's port at low tide.
        </p>
        <p>
        Bhavnagar was founded by Bhavsinhji Gohil (1703-64 AD) in 1723 AD near the Gulf of Khambhat, on a carefully chosen strategic location having potential of maritime trade. Till independence, Bhavnagar State was ruled by the Gohil Rajput clan. The Gohil Rajputs came to Gujarat from Marwar in 1260 AD and had 3 capitals- Sejakpur, Umrala and Sihor; before finally establishing Bhavnagar as their capital. The old town of Bhavnagar was a fortified town with gates leading to other important towns of the region. The Darbargadh (royal residence) was located at the center of the town. The rulers of Bhavnagar subsequently shifted to Motibag and Nilambag Palace. Bhavnagar remained a major port, for almost two centuries, trading commodities with Africa, Mozambique, Zanzibar, Singapore and the Persian Gulf. This flourishing maritime trade resulted in the high rate of urbanization surplus wealth and cosmopolitan culture of the town. 
        </p>
        <P>Present day Bhavnagar owes much to the vision of the past ruler Sir Takhtsinhji Gohil (1869-96 AD). The town planning schemes were designed and implemented under the guidance of progressive rulers of Bhavnagar. During the reign of Sir Takhtsinhji, the British State Engineer Proctor Sims supervised the construction of Barton Library, High Court, Alfred High School and Sir Takhtsinhji Hospital.</P>
        <P>Takhteshwar Temple (1893 AD) is located on a hill, on a high plinth, offering a commanding view of Bhavnagar. The high shikhara rising above the pillared rectangular mandapa makes it an important landmark.</P>
        <P>Barton Library (1895 AD) is a beautiful two storeyed building, appropriately designed for a road junction. It has two wings and a central tower, constructed in ashlar stone masonry, with Gothic arch windows and a sloping roof with Mangalore tiles. It is one of the oldest libraries of Gujarat and also houses a museum.</P>
        <p>Gangajalia (1893 AD) is a temple dedicated to Gangadevi, with a chhatri, pavilion and bridge all in white marble. It is located in the middle of a former tank. It was designed by Sir John Griffith, principal of Sir J J School of Arts, Bombay.</p>
        <p>Nilambag Palace, now a heritage hotel, is a magnificent building set amidst a huge estate of 10 acres. It was designed by a German architect, as a royal residence in 1859 AD. It combines elements of Indian architecture with a modern outlook.</p>
        <p>Town Hall (1932 AD) was formerly a Darbar Hall, where the coronation of Sir Krishnakumarsinhji took place. It is an imposing structure in the colonial style, set in a well laid out garden. Victoria Park covers an area of about 500 acres with diverse flora and fauna. This park is a paradise for bird watchers and nature lovers.</p>
        <p>Gandhi Smriti is a memorial, with a library of Gandhian books and photographs about the life of Mahatma Gandhi. It also has a fine collection of art objects representing the culture of the Saurashtra region.</p>
        <p>Bhavnagar has many good educational institutes with excellent architecture- Sir P P Science Institute, Shamaldas College (Mahatma Gandhi studied here in 1884 AD), Alfred High School and Central Salt Research Institute.</p>
